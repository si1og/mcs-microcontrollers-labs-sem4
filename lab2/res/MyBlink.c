// MyBlink.c (optimized version)

int main()
{
    int i;
    unsigned long int j;

    i = 0;
    j = 0;

    //========== 1. ВКЛЮЧЕНИЕ ТАКТИРОВАНИЯ ПОРТОВ ==========
    // Адрес 0x40023830 - это регистр RCC_AHB1ENR (управление тактированием)
    // Устанавливаем биты 6, 7, 8 для включения портов G, H, I
    *(unsigned long*)(0x40023830) |= (0x40 | 0x80 | 0x100);

    for (i = 0; i < 4; i++) {} // Небольшая задержка для готовности GPIO

    //========== 2. НАСТРОЙКА ПИНОВ КАК ВЫХОДОВ ==========
    
    // Порт G (адрес 0x40021800 - GPIOG_MODER):
    // Настраиваем PG6 (биты 12-13), PG7 (биты 14-15) и PG8 (биты 16-17) как выходы (01)
    *(unsigned long*)(0x40021800) &= ~((3 << 12) | (3 << 14) | (3 << 16)); // Очищаем биты
    *(unsigned long*)(0x40021800) |=  ((1 << 12) | (1 << 14) | (1 << 16)); // Устанавливаем в 01
			
    // Порт H (адрес 0x40021C00 - GPIOH_MODER):
    // Настраиваем PH2, PH3, PH6, PH7 как выходы
    *(unsigned long*)(0x40021C00) &= ~((3 << 4) | (3 << 6) | (3 << 12) | (3 << 14));
    *(unsigned long*)(0x40021C00) |=  ((1 << 4) | (1 << 6) | (1 << 12) | (1 << 14));

    // Порт I (адрес 0x40022000 - GPIOI_MODER):
    // Настраиваем PI10 (биты 20-21) как выход
    *(unsigned long*)(0x40022000) &= ~(3 << 20);
    *(unsigned long*)(0x40022000) |=  (1 << 20);

    // Инициализируем все светодиоды выключенными перед циклом
    *(unsigned long*)(0x40021814) &= ~(0x40 | 0x80 | 0x100);           // PG6, PG7, PG8 выкл
    *(unsigned long*)(0x40021C14) &= ~(0x04 | 0x08 | 0x40 | 0x80);     // Все H выкл
    *(unsigned long*)(0x40022014) &= ~(0x400);                         // PI10 выкл

    //========== 3. ОСНОВНОЙ ЦИКЛ УПРАВЛЕНИЯ СВЕТОДИОДАМИ ==========
    while (1)
    {
        // --- ШАГ 1: ВКЛЮЧАЕМ ПАРУ 1 (PH3, PH6) ---
        *(unsigned long*)(0x40021C14) |= (0x08 | 0x40);  // PH3, PH6 вкл
        for (j = 0; j < 4000000; j++) {} // Задержка

        // Выключаем пару 1 перед шагом 2
        *(unsigned long*)(0x40021C14) &= ~(0x08 | 0x40);  // PH3, PH6 выкл

        // --- ШАГ 2: ВКЛЮЧАЕМ ПАРУ 2 (PH7, PI10) ---
        *(unsigned long*)(0x40021C14) |= 0x80;   // PH7 вкл
        *(unsigned long*)(0x40022014) |= 0x400;  // PI10 вкл
        for (j = 0; j < 4000000; j++) {} // Задержка

        // Выключаем пару 2 перед шагом 3
        *(unsigned long*)(0x40021C14) &= ~0x80;   // PH7 выкл
        *(unsigned long*)(0x40022014) &= ~0x400;  // PI10 выкл

        // --- ШАГ 3: ВКЛЮЧАЕМ ПАРУ 3 (PG6, PG7) ---
        *(unsigned long*)(0x40021814) |= (0x40 | 0x80);  // PG6, PG7 вкл
        for (j = 0; j < 4000000; j++) {} // Задержка

        // Выключаем пару 3 перед шагом 4
        *(unsigned long*)(0x40021814) &= ~(0x40 | 0x80);  // PG6, PG7 выкл

        // --- ШАГ 4: ВКЛЮЧАЕМ ПАРУ 4 (PG8, PH2) ---
        *(unsigned long*)(0x40021814) |= 0x100;  // PG8 вкл
        *(unsigned long*)(0x40021C14) |= 0x04;   // PH2 вкл
        for (j = 0; j < 4000000; j++) {} // Задержка

        // Выключаем пару 4 перед возвратом к шагу 1
        *(unsigned long*)(0x40021814) &= ~0x100;  // PG8 выкл
        *(unsigned long*)(0x40021C14) &= ~0x04;   // PH2 выкл
    }
}